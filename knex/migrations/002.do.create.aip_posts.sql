CREATE TYPE post_type_category AS ENUM (
    'lifestyle',
    'podcast',
    'recipe',
    'event',
    'book'
);

CREATE TABLE aip_posts(
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id INTEGER REFERENCES aip_users(id) ON DELETE CASCADE NOT NULL,
    title TEXT,
    link TEXT,
    event_dates TEXT,
    by TEXT,
    content TEXT,
    post_type post_type_category NOT NULL,
    date_created TIMESTAMPTZ NOT NULL DEFAULT now()
) 